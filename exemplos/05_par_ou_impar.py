numero = int(input("Digite um número: "))  # Solicita um número ao usuário

# Verifica se o número é par ou ímpar
if numero % 2 == 0:
    print(f"{numero} é par.")  # Exibe que o número é par
else:
    print(f"{numero} é ímpar.")  # Exibe que o número é ímpar