def soma(a, b):
    # Função que calcula a soma de dois números
    return a + b

# Exemplo de uso da função
numero1 = 5
numero2 = 3
resultado = soma(numero1, numero2)

print(f"A soma de {numero1} e {numero2} é: {resultado}")