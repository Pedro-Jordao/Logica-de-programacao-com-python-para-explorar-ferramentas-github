# Este arquivo demonstra como criar e manipular listas em Python.

# Criando uma lista de frutas
frutas = ["maçã", "banana", "laranja"]

# Imprimindo a lista original
print("Lista original:", frutas)

# Adicionando uma fruta à lista
frutas.append("uva")
print("Após adicionar uva:", frutas)

# Removendo uma fruta da lista
frutas.remove("banana")
print("Após remover banana:", frutas)

# Acessando um elemento da lista
print("Primeira fruta da lista:", frutas[0])

# Mostrando o tamanho da lista
print("Número de frutas na lista:", len(frutas))