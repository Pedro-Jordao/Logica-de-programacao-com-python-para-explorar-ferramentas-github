# Este arquivo calcula a área de um retângulo.
largura = float(input("Digite a largura do retângulo: "))  # Solicita a largura
altura = float(input("Digite a altura do retângulo: "))    # Solicita a altura

area = largura * altura  # Calcula a área
print(f"A área do retângulo é: {area}")  # Exibe o resultado