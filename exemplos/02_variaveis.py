# Este arquivo demonstra como declarar e usar variáveis em Python.

# Declarando variáveis de diferentes tipos
nome = "João"  # String
idade = 25     # Inteiro
altura = 1.75  # Float
ativo = True   # Booleano

# Exibindo as variáveis
print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)
print("Ativo:", ativo)

# Exemplo de concatenação de strings
mensagem = "Meu nome é " + nome + " e tenho " + str(idade) + " anos."
print(mensagem)